                                              <strong>6.TREATMENT IS VERY EFFECTIVE—IF YOU NEED IT.</strong>  <br>
											  Levothyroxine—synthetic T4—is one of the most prescribed drugs in the U.S., more common than amoxicillin or Lipitor. It's relatively inexpensive (about $13 per month for the generic form) and has few side effects. Most patients notice a difference in how they feel within a month. Although the medication is safe, you need to take it at the same time every morning on an empty stomach, an hour before eating. If it works, you need to be on it in some form for the rest of your life, says Juan Brito, M.D., an endocrinologist at the Mayo Clinic, and thus experts caution against the pills unless you truly have a thyroid disorder.</p>
